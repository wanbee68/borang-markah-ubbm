async function run() {
  const students: any[] = [];
  
  ocrData.forEach((page, pageIdx) => {
    const lines = page.split('\n');
    const tingkatan = pageIdx < 3 ? '4' : '5';
    
    lines.forEach(line => {
      const parts = line.trim().split(/\s+/);
      if (parts.length < 3) return;
      
      const bil = parts[0];
      const kelas = parts[parts.length - 1];
      const maktabId = parts[parts.length - 2];
      const name = parts.slice(1, parts.length - 2).join(' ');
      
      students.push({
        name: name.toUpperCase(),
        maktabId,
        tingkatan,
        kelas: kelas.toUpperCase(),
        jantina: 'L' // Default to L, should be updated manually if needed or derived
      });
    });
  });

  console.log(`Parsed ${students.length} students.`);

  // Generate Excel
  const worksheet = XLSX.utils.json_to_sheet(students);
  const workbook = XLSX.utils.book_new();
  XLSX.utils.book_append_sheet(workbook, worksheet, "Senarai Pelajar");
  XLSX.writeFile(workbook, "students_list.xlsx");
  console.log("Excel file generated: students_list.xlsx");

  // Save to Firestore
  for (const student of students) {
    try {
      await addDoc(collection(db, 'artifacts', appId, 'public', 'data', 'students'), student);
    } catch (err) {
      console.error(`Gagal menyimpan ${student.name}:`, err);
    }
  }
  console.log("Students stored in Firestore database.");
  process.exit(0);
}

run();
